#!/bin/bash
# Initialize an Obsidian vault for a research paper project
# Usage: bash init_vault.sh "project-name" "/output/path"

set -euo pipefail

PROJECT_NAME="${1:?Usage: init_vault.sh <project-name> <output-path>}"
OUTPUT_PATH="${2:?Usage: init_vault.sh <project-name> <output-path>}"

VAULT_ROOT="${OUTPUT_PATH}/${PROJECT_NAME}"

if [ -d "$VAULT_ROOT" ]; then
    echo "Warning: Vault already exists at ${VAULT_ROOT}. Skipping initialization."
    exit 0
fi

