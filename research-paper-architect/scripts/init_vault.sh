# Writing Guide
cat > "${VAULT_ROOT}/06 - Writing Guide/Voice DNA.md" << 'EOF'
# Voice DNA

See [[Banned Phrases]] for the kill list.

## Core Rules
1. Write technically and academically
2. Get to the point. No throat-clearing
3. Be specific: numbers, names, concrete details
4. Vary sentence length
5. Use physical verbs: "stripped back" not "simplified"
6. Parenthetical asides for editorial commentary
7. Never pad output

## Formatting
- Numbers as digits with commas
- NO em dashes. Use commas, periods, colons, semicolons, or parentheses
- Bold sparingly
EOF

cat > "${VAULT_ROOT}/06 - Writing Guide/Banned Phrases.md" << 'EOF'
# Banned Phrases

If any of these appear in output, the output fails. No exceptions.

## Dead AI Language
- "In today's [anything]..."
- "It's important/worth noting..."
- "Delve" / "Dive into" / "Unpack"
- "Harness" / "Leverage" / "Utilize"
- "Landscape" / "Realm" / "Robust"
- "Game-changer" / "Cutting-edge"
- "Straightforward"
- "In order to"

## Dead Transitions
- "Moving forward" / "At the end of the day"
- "To put this in perspective..."
- "What makes this particularly interesting is..."
- "In other words..."

## FATAL Pattern
- "This isn't X. This is Y." and ALL variations
- "Not X. Y." / "Forget X. This is Y." / "Less X, more Y."
- Any sentence that negates one framing then asserts a corrected one
EOF

